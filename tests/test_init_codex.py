class ParallelReviewReportTests(unittest.TestCase):
    def test_run_parallel_reviews_keeps_empty_report_when_output_is_unstructured(self):
        with tempfile.TemporaryDirectory() as tmpdir:
            repo_dir = Path(tmpdir) / "repo"
            repo_dir.mkdir()
            output_dir = Path(tmpdir) / "out"
            output_dir.mkdir()
            context = {
                "repo_dir": str(repo_dir),
                "owner": "owner",
                "repo": "repo",
                "pr_id": "123",
                "changed_files": ["src/main.py"],
                "changed_files_count": 1,
            }

            def fake_gemini(repo_dir, prompt, output_file):
                output_file.write_text("No issues found.\n")
                return output_file, ["No issues found."]

            with patch("scripts.run_review.run_gemini_agent", side_effect=fake_gemini):
                review_reports, all_issues = run_review.run_parallel_reviews(
                    repo_dir=repo_dir,
                    context=context,
                    output_dir=output_dir,
                    use_claude=False,
                    use_gemini=True,
                    use_codex=False,
                )

            self.assertEqual(all_issues["gemini"], [])
            self.assertEqual(review_reports["gemini"], output_dir / "gemini_review.md")
            self.assertTrue((output_dir / "gemini_review.md").exists())
            self.assertTrue((output_dir / "gemini_review.html").exists())
            self.assertTrue((output_dir / "gemini_review.json").exists())
            self.assertIn(
                "No structured issues were parsed from the reviewer output.",
                (output_dir / "gemini_review.md").read_text(),
            )

    def test_run_parallel_reviews_defaults_codex_reasoning_effort_to_xhigh(self):
        with tempfile.TemporaryDirectory() as tmpdir:
            repo_dir = Path(tmpdir) / "repo"
            repo_dir.mkdir()
            output_dir = Path(tmpdir) / "out"
            output_dir.mkdir()
            context = {
                "repo_dir": str(repo_dir),
                "owner": "owner",
                "repo": "repo",
                "pr_id": "123",
                "changed_files": ["src/main.py"],
                "changed_files_count": 1,
            }

            def fake_codex(repo_dir, prompt, output_file, **kwargs):
                self.assertEqual(kwargs["reasoning_effort"], "xhigh")
                output_file.write_text("")
                return output_file, []

            with patch("scripts.run_review.run_codex_review_agent", side_effect=fake_codex):
                run_review.run_parallel_reviews(
                    repo_dir=repo_dir,
                    context=context,
                    output_dir=output_dir,
                    use_claude=False,
                    use_gemini=False,
                    use_codex=True,
                )

    def test_run_parallel_reviews_can_run_opencode(self):
        with tempfile.TemporaryDirectory() as tmpdir:
            repo_dir = Path(tmpdir) / "repo"
            repo_dir.mkdir()
            output_dir = Path(tmpdir) / "out"
            output_dir.mkdir()
            context = {
                "repo_dir": str(repo_dir),
                "owner": "owner",
                "repo": "repo",
                "pr_id": "123",
                "changed_files": ["src/main.py"],
                "changed_files_count": 1,
            }

            issue_block = "\n".join(
                [
                    "===ISSUE===",
                    "FILE: src/main.py",
                    "LINE: 7",
                    "SEVERITY: medium",
                    "TITLE: Example issue",
                    "PROBLEM: Example problem.",
                    "CODE:",
                    "```",
                    "bad()",
                    "```",
                    "FIX:",
                    "```",
                    "good()",
                    "```",
                    "===END===",
                ]
            )

            def fake_opencode(repo_dir, prompt, output_file, **kwargs):
                output_file.write_text(issue_block)
                return output_file, issue_block.splitlines()

            with patch("scripts.run_review.run_opencode_agent", side_effect=fake_opencode):
                review_reports, all_issues = run_review.run_parallel_reviews(
                    repo_dir=repo_dir,
                    context=context,
                    output_dir=output_dir,
                    use_claude=False,
                    use_gemini=False,
                    use_codex=False,
                    use_opencode=True,
                )

            self.assertEqual(review_reports["opencode"], output_dir / "opencode_review.md")
            self.assertEqual(len(all_issues["opencode"]), 1)
            self.assertEqual(all_issues["opencode"][0]["title"], "Example issue")


