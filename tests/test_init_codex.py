class InitCodexTests(unittest.TestCase):
    def test_init_codex_skips_when_agents_exists(self):
        with tempfile.TemporaryDirectory() as tmpdir:
            repo_dir = Path(tmpdir)
            agents_md = repo_dir / "AGENTS.md"
            agents_md.write_text("existing\n")

            with patch("scripts.run_review.subprocess.run") as mock_run:
                self.assertTrue(run_review.init_codex(repo_dir))
                mock_run.assert_not_called()

    def test_init_codex_writes_last_message_to_agents_md(self):
        with tempfile.TemporaryDirectory() as tmpdir:
            repo_dir = Path(tmpdir)

            def fake_run(cmd, **kwargs):
                self.assertIn("--output-last-message", cmd)
                output_path = Path(cmd[cmd.index("--output-last-message") + 1])
                output_path.write_text("# Project Overview\nGenerated by test.\n")
                return subprocess.CompletedProcess(cmd, 0, "", "")

            with patch("scripts.run_review.shutil.which", return_value="/usr/bin/codex"):
                with patch("scripts.run_review.subprocess.run", side_effect=fake_run) as mock_run:
                    self.assertTrue(run_review.init_codex(repo_dir))
                    self.assertTrue((repo_dir / "AGENTS.md").exists())
                    self.assertIn("Generated by test.", (repo_dir / "AGENTS.md").read_text())
                    self.assertEqual(mock_run.call_count, 1)


class InitOpenCodeTests(unittest.TestCase):
    def test_init_opencode_skips_when_agents_exists(self):
        with tempfile.TemporaryDirectory() as tmpdir:
            repo_dir = Path(tmpdir)
            agents_md = repo_dir / "AGENTS.md"
            agents_md.write_text("existing\n")

            with patch("scripts.run_review.subprocess.run") as mock_run:
                self.assertTrue(run_review.init_opencode(repo_dir))
                mock_run.assert_not_called()

    def test_init_opencode_writes_stdout_to_agents_md(self):
        with tempfile.TemporaryDirectory() as tmpdir:
            repo_dir = Path(tmpdir)

            with patch("scripts.run_review.shutil.which", return_value="/usr/bin/opencode"):
                with patch(
                    "scripts.run_review.subprocess.run",
                    return_value=subprocess.CompletedProcess(
                        ["opencode", "run"],
                        0,
                        "# Project Overview\nGenerated by OpenCode.\n",
                        "",
                    ),
                ) as mock_run:
                    self.assertTrue(run_review.init_opencode(repo_dir))
                    self.assertTrue((repo_dir / "AGENTS.md").exists())
                    self.assertIn("Generated by OpenCode.", (repo_dir / "AGENTS.md").read_text())
                    self.assertEqual(mock_run.call_count, 1)

    def test_init_agents_md_falls_back_to_opencode(self):
        with tempfile.TemporaryDirectory() as tmpdir:
            repo_dir = Path(tmpdir)

            with patch("scripts.run_review.init_codex", return_value=False) as mock_codex:
                with patch("scripts.run_review.init_opencode", return_value=True) as mock_opencode:
                    self.assertTrue(
                        run_review.init_agents_md(
                            repo_dir,
                            use_codex=True,
                            use_opencode=True,
                        )
                    )

        mock_codex.assert_called_once()
        mock_opencode.assert_called_once_with(repo_dir)


class CodexCommandTests(unittest.TestCase):
    def test_build_codex_command_bypasses_sandbox_by_default(self):
        cmd = run_review.build_codex_command(
            Path("/tmp/repo")
        )

        self.assertIn("-c", cmd)
        self.assertIn(
            f'model_reasoning_effort="{run_review.DEFAULT_CODEX_REASONING_EFFORT}"',
            cmd,
        )
        self.assertIn("--dangerously-bypass-approvals-and-sandbox", cmd)
        self.assertNotIn("--full-auto", cmd)
        self.assertEqual(cmd[-1], "-")

    def test_build_codex_command_can_use_sandbox(self):
        cmd = run_review.build_codex_command(
            Path("/tmp/repo"),
            use_sandbox=True
        )

        self.assertIn("--full-auto", cmd)
        self.assertNotIn("--dangerously-bypass-approvals-and-sandbox", cmd)
        self.assertEqual(cmd[-1], "-")

    def test_build_codex_command_can_set_model_and_reasoning_effort(self):
        cmd = run_review.build_codex_command(
            Path("/tmp/repo"),
            model=run_review.CODEX_SPARK_MODEL,
            reasoning_effort="xhigh",
        )

        self.assertIn("--model", cmd)
        self.assertIn(run_review.CODEX_SPARK_MODEL, cmd)
        self.assertIn("-c", cmd)
        self.assertIn('model_reasoning_effort="xhigh"', cmd)
        self.assertEqual(cmd[-1], "-")

    def test_build_claude_command_passes_prompt_as_argument(self):
        cmd = run_review.build_claude_command(
            "review this change",
            append_system_prompt="only issue blocks",
        )

        self.assertEqual(cmd[0:2], ["claude", "-p"])
        self.assertIn("--append-system-prompt", cmd)
        self.assertEqual(cmd[-1], "review this change")
        self.assertNotIn("-", cmd[2:])

    def test_build_gemini_command_uses_prompt_flag(self):
        cmd = run_review.build_gemini_command("review this change")

        self.assertEqual(cmd, ["gemini", "-p", "review this change", "-y"])

    def test_build_opencode_command_skips_permissions_by_default(self):
        cmd = run_review.build_opencode_command("review this change")

        self.assertEqual(cmd[0:2], ["opencode", "run"])
        self.assertIn("--dangerously-skip-permissions", cmd)
        self.assertEqual(cmd[-1], "review this change")

    def test_build_opencode_command_can_set_model(self):
        cmd = run_review.build_opencode_command(
            "review this change",
            model="anthropic/claude-sonnet-4",
        )

        self.assertIn("--model", cmd)
        self.assertIn("anthropic/claude-sonnet-4", cmd)


